#adding list items
fruits=["banana", "apple", "cherry", "kiwi", "mango"]
fruits.append("orange")
fruits.append("pineapple")
print(fruits)

fruits=["banana", "apple", "cherry", "kiwi", "mango", "orange"]
fruits.extend("orange")
print(fruits)

fruits=["banana", "apple", "cherry", "kiwi", "mango", "orange"]
fruits.insert(2, "ovacado")
print(fruits)



