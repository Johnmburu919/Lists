#change list items
fruits=["banana", "apple", "cherry", "kiwi", "mango"]
fruits[1]="blackcurrant" #change the second item
print(fruits)

fruits=["banana", "apple", "cherry", "kiwi", "mango"]
fruits[1:3]=["watermelon"]#changes the second and third value by replacing it with one value
print(fruits)

